package com.spring.bbs.project.dto;

public class ItemInfoDto {
	int i_num;
	String i_title;
	String i_content;
	String i_image;
	String i_category;
	
	public ItemInfoDto(int i_num, String i_title, String i_content, String i_image, String i_category) {
		super();
		this.i_num = i_num;
		this.i_title = i_title;
		this.i_content = i_content;
		this.i_image = i_image;
		this.i_category = i_category;
	}

	public int getI_num() {
		return i_num;
	}

	public void setI_num(int i_num) {
		this.i_num = i_num;
	}

	public String getI_title() {
		return i_title;
	}

	public void setI_title(String i_title) {
		this.i_title = i_title;
	}

	public String getI_content() {
		return i_content;
	}

	public void setI_content(String i_content) {
		this.i_content = i_content;
	}

	public String getI_image() {
		return i_image;
	}

	public void setI_image(String i_image) {
		this.i_image = i_image;
	}

	public String getI_category() {
		return i_category;
	}

	public void setI_category(String i_category) {
		this.i_category = i_category;
	}

	
	
	

	
	
	
	
	

}//-----------
